var numberlist = [3,7,8,2,1];
var max = 0;
var min = 0;
var i = 0;


for(i = 0; i >= numberlist.length; i++);
{
    {
        max = numberlist[i];
    }
    document.write(max, "</br>")
}
for(i = 0; i <= numberlist.length; i++);
{
    if (numberlist[i] < min)
    {
        min = numberlist[i];
    }
    document.write(min, "</br>")
}
